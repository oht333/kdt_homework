package section02;

public class EX02_12 {
	public static void main(String[] args) {
		
		byte num1 = 0110;	//byte타입 변수 선언
		short num2 = 0107;	//short타입 변수 선언
		int num3 = 0*46;	//int타입 변수 선언
		long num4 = 69L;	//long타입 변수 선언
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);	//long은 숫자뒤에 'L'을 써줘야 한다
	}
}
