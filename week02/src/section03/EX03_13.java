package section03;

public class EX03_13 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		System.out.println(a > b);	// 10 > 20	: false
		System.out.println(a <= b);	// 10 <= 20	: true
		System.out.println(a == b);	// 10 == 20 : false
		System.out.println(a != b);	// 10 != 20 : true
	}

}
